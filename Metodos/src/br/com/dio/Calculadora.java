package br.com.dio;

public class Calculadora {
  public static Double sum(Double valueA, Double valueB) {
    return valueA + valueB;
  }

  public static Double sub(Double valueA, Double valueB) {
    return valueA - valueB;
  }

  public static Double div(Double valueA, Double valueB) {
    return valueA / valueB;
  }

  public static Double mul(Double valueA, Double valueB) {
    return valueA * valueB;
  }
}
